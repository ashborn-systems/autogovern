"""The generation engine.

A deep module: one entry point (``generate_docs``) over a pure core that
resolves declared inputs, hashes them, calls the provider only for sections
whose inputs changed, and writes content-addressed documents atomically.

Regeneration is deterministic and free: the section dependency graph (Phase 6)
plus per-section input hashes decide what to re-render with zero LLM calls.
A second generate with no input changes writes nothing, so the idempotence
gate holds.

No verifier pass. The docs are generated from the scan; they match reality
by construction. ATTENTION.md is an informational note about missing context,
not a human review queue.
"""

from __future__ import annotations

import hashlib
from dataclasses import dataclass, field
from pathlib import Path

from autogovern.frameworks import DocumentFeed, Pack, load_pack
from autogovern.generate.frontmatter import (
    build_frontmatter,
    existing_section_hashes,
    now_iso,
    parse_frontmatter,
    render_document,
)
from autogovern.generate.inputs import (
    compute_section_hash,
    extract_input,
    profile_file_hashes,
)
from autogovern.generate.lockfile import write_lockfile
from autogovern.generate.prompts import build_section_messages
from autogovern.generate.writer import write_if_changed
from autogovern.models import AgentProfile, Config, ContextManifest
from autogovern.provider import ProviderClient

GOVERNANCE_DIR = Path("governance")

# Documents generated by the engine without an LLM call. QUICKSTART and
# ATTENTION are non-disableable per the spec.
ENGINE_DOCS_ALWAYS = ["QUICKSTART.md", "ATTENTION.md"]

# LLM-fed documents, in spec table order.
LLM_DOCS = [
    "system-card.md",
    "risk-assessment.md",
    "data-protection.md",
    "oversight.md",
    "inventory.md",
    "testing.md",
    "incident-response.md",
]


@dataclass
class GenerationResult:
    """Outcome of a generate run, for the CLI to print and tests to assert."""

    governance_dir: Path
    regenerated: list[str] = field(default_factory=list)
    skipped: list[str] = field(default_factory=list)
    llm_call_count: int = 0
    written_files: list[Path] = field(default_factory=list)
    pack_version: str = ""

    @property
    def changed(self) -> bool:
        return bool(self.regenerated)


def generate_docs(
    root: Path,
    config: Config,
    profile: AgentProfile,
    context: ContextManifest,
    *,
    provider: ProviderClient,
    pack: Pack | None = None,
    context_from_file: bool = True,
) -> GenerationResult:
    """Generate or incrementally update the governance document set.

    Writes into ``<root>/governance/``. Only sections whose input hash changed
    are re-rendered (one LLM call each); the rest are left untouched. The
    profile lockfile is written on every run (content-addressed, so a no-op
    when the profile is unchanged).

    When ``context_from_file`` is False (vanilla mode), ATTENTION.md notes
    that docs are generic because no context manifest was loaded.

    The provider lifecycle is owned by the caller; this function does not
    close it.
    """
    pack = pack or load_pack()
    governance_dir = root / GOVERNANCE_DIR
    governance_dir.mkdir(parents=True, exist_ok=True)

    result = GenerationResult(governance_dir=governance_dir, pack_version=pack.version)
    file_hashes = profile_file_hashes(profile)
    enabled = _enabled_documents(config)
    timestamp = now_iso()

    for doc in LLM_DOCS:
        if doc not in enabled:
            continue
        feed = pack.document_feeds.get(doc)
        if feed is None:  # pragma: no cover - pack and config should agree
            continue
        _maybe_regenerate_llm_doc(
            governance_dir / doc,
            doc,
            feed,
            profile,
            context,
            provider,
            pack,
            file_hashes,
            result,
            timestamp,
        )

    _write_engine_docs(governance_dir, config, pack, profile, result, timestamp, context_from_file)
    write_lockfile(governance_dir, profile)
    return result


# ---------------------------------------------------------------------------
# LLM-fed documents
# ---------------------------------------------------------------------------


def _maybe_regenerate_llm_doc(
    path: Path,
    doc: str,
    feed: DocumentFeed,
    profile: AgentProfile,
    context: ContextManifest,
    provider: ProviderClient,
    pack: Pack,
    file_hashes: dict[str, str],
    result: GenerationResult,
    timestamp: str,
) -> None:
    declared = _resolve_declared_inputs(feed, profile, context)
    new_hash = compute_section_hash(declared, [s.content for s in feed.pack_sections], pack.version)

    existing_hash = _existing_hash(path, doc)
    if existing_hash == new_hash and path.is_file():
        result.skipped.append(doc)
        return

    messages = build_section_messages(doc, feed, declared, pack.style_authority)
    body = provider.chat(messages)
    result.llm_call_count += 1
    result.regenerated.append(doc)

    frontmatter = build_frontmatter(
        doc_version=_short_hash(new_hash),
        agent_version=profile.version,
        generated=timestamp,
        generator_version=_generator_version(),
        input_hashes=file_hashes,
        framework_pack_version=pack.version,
        section_hashes={doc: new_hash},
    )
    if write_if_changed(path, render_document(frontmatter, body)):
        result.written_files.append(path)


def _resolve_declared_inputs(
    feed: DocumentFeed, profile: AgentProfile, context: ContextManifest
) -> dict[str, object]:
    declared: dict[str, object] = {}
    for path in feed.profile_inputs:
        declared[path] = extract_input(path, profile, context)
    for path in feed.context_inputs:
        declared[path] = extract_input(path, profile, context)
    return declared


def _existing_hash(path: Path, doc: str) -> str | None:
    if not path.is_file():
        return None
    return existing_section_hashes(path.read_text(encoding="utf-8")).get(doc)


# ---------------------------------------------------------------------------
# Engine-generated documents
# ---------------------------------------------------------------------------


def _write_engine_docs(
    governance_dir: Path,
    config: Config,
    pack: Pack,
    profile: AgentProfile,
    result: GenerationResult,
    timestamp: str,
    context_from_file: bool,
) -> None:
    file_hashes = profile_file_hashes(profile)
    common = _Common(
        agent_version=profile.version,
        generator_version=_generator_version(),
        input_hashes=file_hashes,
        framework_pack_version=pack.version,
    )

    # QUICKSTART: regenerate only if something changed this run.
    if result.changed:
        body = _quickstart_body(profile, result, context_from_file)
        fm = _engine_frontmatter("QUICKSTART", _short_hash(body), timestamp, common)
        if _write_engine_doc_if_body_changed(governance_dir / "QUICKSTART.md", fm, body):
            result.written_files.append(governance_dir / "QUICKSTART.md")

    # ATTENTION: informational note about context completeness.
    att_body = _attention_body(context_from_file)
    att_fm = _engine_frontmatter("ATTENTION", _short_hash(att_body), timestamp, common)
    if _write_engine_doc_if_body_changed(governance_dir / "ATTENTION.md", att_fm, att_body):
        result.written_files.append(governance_dir / "ATTENTION.md")

    # CHANGELOG: append-only, one entry per run that regenerated something.
    if result.changed:
        _append_changelog(governance_dir / "CHANGELOG.md", common, result, timestamp)


def _write_engine_doc_if_body_changed(
    path: Path, frontmatter: dict[str, object], body: str
) -> bool:
    """Write only if the body differs from the existing file's body.

    Comparing bodies (not full content) means an unchanged engine doc keeps
    its existing frontmatter and ``generated`` timestamp, which is what makes
    a no-op generate produce zero git diff.
    """
    if path.is_file():
        _, existing_body = parse_frontmatter(path.read_text(encoding="utf-8"))
        if existing_body.lstrip("\n") == body.lstrip("\n"):
            return False
    return write_if_changed(path, render_document(frontmatter, body))


def _quickstart_body(
    profile: AgentProfile, result: GenerationResult, context_from_file: bool
) -> str:
    lines = [
        f"# Governance quickstart for {profile.name}",
        "",
        f"Agent version: {profile.version}",
        "",
        "## What was generated",
        "",
        "The governance document set in this directory was generated by autogovern.",
        "",
    ]
    for doc in result.regenerated:
        lines.append(f"- `{doc}` regenerated")
    lines.extend(
        [
            "",
            "## How to read it",
            "",
            "Each document carries YAML frontmatter recording its version, the input",
            "hashes it was built from, and when it was generated.",
            "",
        ]
    )
    if not context_from_file:
        lines.extend(
            [
                "## Note",
                "",
                "Running without a context manifest. Docs are generic. Run",
                "`autogovern init` for specific governance docs.",
                "",
            ]
        )
    return "\n".join(lines)


def _attention_body(context_from_file: bool) -> str:
    """The ATTENTION.md body — informational, not a work queue."""
    if context_from_file:
        return "# Attention ledger\n\nNo attention items. The governance set is fully specified.\n"
    return (
        "# Attention ledger\n\n"
        "Running without a context manifest. Docs are generic. Run "
        "`autogovern init` to provide organisational context (risk appetite, "
        "oversight model, jurisdictions, owner) for specific governance docs.\n"
    )


def _append_changelog(
    path: Path, common: _Common, result: GenerationResult, timestamp: str
) -> None:
    existing = path.read_text(encoding="utf-8") if path.is_file() else ""
    _, body_existing = parse_frontmatter(existing) if existing else ({}, "")
    entry = f"## {timestamp} — regenerated: {', '.join(result.regenerated)}\n"
    new_body = (body_existing.lstrip("\n") + "\n" + entry) if body_existing.strip() else entry
    fm = _engine_frontmatter("CHANGELOG", _short_hash(new_body), timestamp, common)
    if write_if_changed(path, render_document(fm, new_body)):
        result.written_files.append(path)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


@dataclass(frozen=True)
class _Common:
    """The frontmatter fields shared across every engine-generated document."""

    agent_version: str
    generator_version: str
    input_hashes: dict[str, str]
    framework_pack_version: str


def _engine_frontmatter(
    doc: str, doc_version: str, generated: str, common: _Common
) -> dict[str, object]:
    return build_frontmatter(
        doc_version=doc_version,
        agent_version=common.agent_version,
        generated=generated,
        generator_version=common.generator_version,
        input_hashes=common.input_hashes,
        framework_pack_version=common.framework_pack_version,
        section_hashes={doc: doc_version},
    )


def _enabled_documents(config: Config) -> set[str]:
    """The enabled document basenames, QUICKSTART and ATTENTION always on."""
    enabled: set[str] = set()
    name_map = {
        "system-card": "system-card.md",
        "risk-assessment": "risk-assessment.md",
        "data-protection": "data-protection.md",
        "oversight": "oversight.md",
        "inventory": "inventory.md",
        "testing": "testing.md",
        "incident-response": "incident-response.md",
        "changelog": "CHANGELOG.md",
    }
    for key, on in config.documents.items():
        if on:
            enabled.add(name_map.get(key, key))
    enabled.update(ENGINE_DOCS_ALWAYS)
    return enabled


def _short_hash(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8")).hexdigest()[:12]


def _generator_version() -> str:
    """The tool version, read once and cached."""
    try:
        from importlib.metadata import version

        return version("autogovern")
    except Exception:  # pragma: no cover - dev installs without metadata
        return "0.0.0+dev"


